#!/usr/bin/env python3
"""
Generate TikZ/PGFPlots bar charts from experiment_results JSON.

The script writes two .tex files:
  1) Width distribution of all clauses (grouping widths > 4)
  2) Width distribution of clauses saved by factor (grouping widths > 4)

Usage:
    python3 src/bicliqueVA/utils/experiment_results_to_tikz_width_bars.py \
        results/experiment_results.json \
        --out-dir latex_plots
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


def load_json(path: Path) -> dict[str, Any]:
    with path.open("r") as f:
        return json.load(f)


def normalize_width_counts(width_counts: dict[Any, Any] | None) -> dict[int, int]:
    normalized: dict[int, int] = {}
    if not width_counts:
        return normalized
    for width, count in width_counts.items():
        w = int(width)
        c = int(count)
        normalized[w] = normalized.get(w, 0) + c
    return normalized


def group_width_counts(
    width_counts: dict[int, int],
    max_explicit_width: int,
    min_included_width: int = 2,
) -> dict[str, int]:
    grouped: dict[str, int] = {
        str(i): 0 for i in range(min_included_width, max_explicit_width + 1)
    }
    overflow_label = f">{max_explicit_width}"
    grouped[overflow_label] = 0

    for width, count in width_counts.items():
        if width < min_included_width:
            continue
        if width <= max_explicit_width:
            grouped[str(width)] += count
        else:
            grouped[overflow_label] += count
    return grouped


def to_percentages(grouped_counts: dict[str, int]) -> dict[str, float]:
    total = sum(grouped_counts.values())
    if total <= 0:
        return {bucket: 0.0 for bucket in grouped_counts.keys()}
    return {bucket: 100.0 * count / total for bucket, count in grouped_counts.items()}


def extract_global_width_counts(data: dict[str, Any]) -> dict[int, int]:
    global_counts = normalize_width_counts(data.get("global_n_clauses_per_width"))
    if global_counts:
        return global_counts

    combined: dict[int, int] = {}
    for stat in data.get("stats_list", []):
        for width, count in normalize_width_counts(stat.get("n_clauses_per_width")).items():
            combined[width] = combined.get(width, 0) + count
    return combined


def extract_factor_saved_width_counts(data: dict[str, Any]) -> dict[int, int]:
    factor_summary = data.get("factor_summary") or {}
    global_saved = normalize_width_counts(factor_summary.get("global_factor_saved_clauses_per_width"))
    if global_saved:
        return global_saved

    combined: dict[int, int] = {}
    for stat in data.get("stats_list", []):
        for width, count in normalize_width_counts(stat.get("factor_saved_clauses_per_width")).items():
            combined[width] = combined.get(width, 0) + count
    return combined


def make_tikz_bar_plot(
    grouped_counts: dict[str, int],
    grouped_percentages: dict[str, float],
    title: str,
    legend_label: str,
) -> str:
    buckets = list(grouped_counts.keys())
    x_coords = ",".join(buckets)
    coords = " ".join(f"({bucket},{grouped_percentages[bucket]:.6f})" for bucket in buckets)
    raw_counts = ", ".join(f"{bucket}={grouped_counts[bucket]}" for bucket in buckets)

    lines: list[str] = []
    lines.append("% Auto-generated by experiment_results_to_tikz_width_bars.py")
    lines.append(f"% Raw counts: {raw_counts}")
    lines.append(r"\begin{tikzpicture}")
    lines.append(r"\begin{axis}[")
    lines.append(r"    ybar,")
    lines.append(r"    bar width=12pt,")
    lines.append(r"    width=0.95\linewidth,")
    lines.append(r"    height=0.55\linewidth,")
    lines.append(r"    ymin=0,")
    lines.append(r"    grid=major,")
    lines.append(r"    ylabel={Percentage (\%)},")
    lines.append(r"    xlabel={Clause width},")
    lines.append(rf"    title={{{title}}},")
    lines.append(rf"    symbolic x coords={{{x_coords}}},")
    lines.append(r"    xtick=data,")
    lines.append(r"    enlarge x limits=0.10,")
    lines.append(r"    legend style={font=\small, at={(0.5,-0.18)}, anchor=north},")
    lines.append(r"]")
    lines.append(
        rf"\addplot+[draw=blue!70!black, fill=blue!45!white] coordinates {{{coords}}};"
    )
    lines.append(rf"\addlegendentry{{{legend_label}}}")
    lines.append(r"\end{axis}")
    lines.append(r"\end{tikzpicture}")
    lines.append("")
    return "\n".join(lines)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate TikZ bar plots for clause width distributions."
    )
    parser.add_argument("input_json", type=Path, help="Path to experiment_results.json")
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=Path("."),
        help="Directory to write output .tex files",
    )
    parser.add_argument(
        "--max-explicit-width",
        type=int,
        default=4,
        help="Largest width to keep explicit; larger widths are grouped into >N",
    )
    parser.add_argument(
        "--min-included-width",
        type=int,
        default=2,
        help="Exclude widths smaller than this from both plot and percentage normalization",
    )
    parser.add_argument(
        "--width-out",
        type=str,
        default="width_distribution_grouped_gt4.tex",
        help="Output filename for global width distribution plot",
    )
    parser.add_argument(
        "--factor-out",
        type=str,
        default="factor_saved_distribution_grouped_gt4.tex",
        help="Output filename for factor saved-width distribution plot",
    )
    args = parser.parse_args()

    data = load_json(args.input_json)

    global_width_counts = extract_global_width_counts(data)
    factor_saved_counts = extract_factor_saved_width_counts(data)

    if args.min_included_width < 1:
        raise SystemExit("--min-included-width must be >= 1")
    if args.min_included_width > args.max_explicit_width:
        raise SystemExit("--min-included-width must be <= --max-explicit-width")

    grouped_global_counts = group_width_counts(
        global_width_counts,
        args.max_explicit_width,
        min_included_width=args.min_included_width,
    )
    grouped_factor_counts = group_width_counts(
        factor_saved_counts,
        args.max_explicit_width,
        min_included_width=args.min_included_width,
    )

    grouped_global_pct = to_percentages(grouped_global_counts)
    grouped_factor_pct = to_percentages(grouped_factor_counts)

    width_tex = make_tikz_bar_plot(
        grouped_counts=grouped_global_counts,
        grouped_percentages=grouped_global_pct,
        title=f"Clause Width Distribution (width >= {args.min_included_width})",
        legend_label="All clauses",
    )
    factor_tex = make_tikz_bar_plot(
        grouped_counts=grouped_factor_counts,
        grouped_percentages=grouped_factor_pct,
        title=f"Factor Saved Clauses by Width (width >= {args.min_included_width})",
        legend_label="Clauses saved by factor",
    )

    out_dir = args.out_dir
    width_out_path = out_dir / args.width_out
    factor_out_path = out_dir / args.factor_out
    write_text(width_out_path, width_tex)
    write_text(factor_out_path, factor_tex)

    print(f"Wrote {width_out_path}")
    print(f"Wrote {factor_out_path}")


if __name__ == "__main__":
    main()
